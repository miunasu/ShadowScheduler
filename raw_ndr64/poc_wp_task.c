/* ============================================================
 * FixupNdr64 - Runtime pointer relocation
 * ============================================================ */
static void FixupNdr64(void)
{
    unsigned char* blob_base = g_Ndr64TypeBlob;
    unsigned char* proc_base = g_Ndr64ProcFmt;
    int i;

    /* Fix pointers inside type blob (127 fixups) */
    for (i = 0; i < sizeof(g_BlobFixups)/sizeof(g_BlobFixups[0]); i++) {unsigned short boff = g_BlobFixups[i].blob_offset;
        unsigned short toff = g_BlobFixups[i].target_offset;
        /* Skip sentinel values (originally negative offsets in source) */
        if (toff == 0xFFFF || toff == 0xFFF3)
            continue;
        *(void**)(blob_base + boff) = (void*)(blob_base + toff);
    }

    /* Fix pointers inside proc format (9 fixups) */
    for (i = 0; i < sizeof(g_ProcFixups)/sizeof(g_ProcFixups[0]); i++) {
        unsigned short poff = g_ProcFixups[i].proc_offset;
        unsigned short toff = g_ProcFixups[i].target_offset;
        *(void**)(proc_base + poff) = (void*)(blob_base + toff);
    }

    /* Setup NDR64 proc table */
    g_Ndr64ProcTable[0] = g_Ndr64ProcFmt;
}

